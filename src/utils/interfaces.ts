export enum Gender {
  Male,
  Female,
  Other,
}

export interface IContactInfo {
  phoneNumber: string;
  emailAddress: string;
  residentialAddress: string;
  nextOfKin: string;
  nextOfKinPhoneNumber: string;
  nextOfKinResidentialAddress: string;
  healthInsured: boolean;
}

export interface IMedicalInfo {
  currentMedications: string;
  allergies: string;
  diagnosis: string;
  treatmentPlan: string;
  medicalHistoryFile: string;
}

export interface IEmergencyContact {
  name: string;
  phoneNumber: string;
  residentialAddress: string;
}

export interface IPatient {
  fullName: string;
  gender: Gender;
  dateOfBirth: bigint;
  contactInfo: IContactInfo;
  medicalInfo: IMedicalInfo;
  isPublished: boolean;
  isActive: boolean;
  emergencyContacts: IEmergencyContact[];
  id: bigint;
}

export interface IRecords {
  _patientAddress: string;
  _fullName: string;
  _gender: Gender;
  _dateOfBirth: number;
  _contactInfo: IContactInfo;
  _medicalInfo: IMedicalInfo;
  _isPublished: boolean;
  _emergencyContacts: IEmergencyContact[];
}

export interface IAppointment {
  name: string;
  date: bigint;
  reason: string;
}
